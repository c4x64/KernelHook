int a64_sysreg_full_lookup(u16 encoding, const char **name)
{
    int lo = 0;
    int hi = (int)(sizeof(a64_sysreg_full_table) /
                   sizeof(a64_sysreg_full_table[0])) - 1;

    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        if (a64_sysreg_full_table[mid].encoding < encoding)
            lo = mid + 1;
        else if (a64_sysreg_full_table[mid].encoding > encoding)
            hi = mid - 1;
        else {
            if (name)
                *name = a64_sysreg_full_table[mid].name;
            return a64_sysreg_full_table[mid].arch_min;
        }
    }
    return -1;
}

